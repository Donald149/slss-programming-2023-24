# Operators that work on Numbers
print(1 + 1)
print(3 - 2)
print(10 * 10)    # Multiply
print(9 / 3)      # Division

print(1 + 2 / 4 + 5 - 4)    # BEDMAS

print(9 // 2)    # Floor division (Rounds down)
print(9  % 2)    # Modulo gives remainder

print(2 ** 4)    # Power function